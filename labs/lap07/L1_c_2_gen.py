def genD():
    """ Генератор для обчислення визначників """
    d2 = 5  # 1-й член послідовності
    yield d2
    d1 = 19  # 2-й член послідовності
    yield d1
    while True:
        d2, d1 = d1, 5 * d1 - 6 * d2
        yield d1


N = int(input("N = "))
for d in genD():
    if d > N:  # якщо згенерований член послідовності перевищує задане число
        break  # прининяємо генерацію членів послідовності
    print("D = %d" % (d)) # виводимо згенерований член послідовності
