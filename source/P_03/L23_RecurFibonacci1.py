def fib(N):
    """ Знаходження елементів послідовності Фібоначчі

    :param N: Номер члена послідовності, що необхідно знайти
    :return:  Знайдений член послідовності.
    """
    f2 = 1
    f1 = 1
    for n in range(2, N + 1):
        f = f1 + f2
        f2 = f1
        f1 = f
    return f1


print("fib(10) = ", fib(10))
